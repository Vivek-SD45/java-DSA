
public class adPattern2 {


    public static void inverted_h_pyramid(int n){
        
       
for(int i=0; i<=n;i++)
{
for(int j=0;j<=n-i+1;j++)
{
System.out.print(j +" ");
}
System.out.println();
}}
    
    
public static void main(String args[]){

    inverted_h_pyramid(7);
}





}

